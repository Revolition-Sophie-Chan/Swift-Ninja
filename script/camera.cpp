#include "camera.h"

//=============================================================================
//静的メンバ変数の初期化
//=============================================================================
CCamera * CCamera::m_pCamera = NULL;
D3DXVECTOR3 CCamera::m_pos = {};

//=============================================================================
// コンストラクタ
//=============================================================================
CCamera::CCamera()
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CCamera::~CCamera()
{
}

//=============================================================================
// ポリゴンの初期化
//=============================================================================
void CCamera::InitCamera(void)
{
	ResetCamera();
}
//=============================================================================
// リセ
//=============================================================================
void CCamera::ResetCamera(void)
{
	m_pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
}
//=============================================================================
// ムーブ
//=============================================================================
void CCamera::MoveCamera(D3DXVECTOR3 m_move)
{

	m_pCamera->m_pos.x += m_move.x;
	m_pCamera->m_pos.y += m_move.y;
}
//=============================================================================
// カメラの取得
//=============================================================================
CCamera * CCamera::GetCamera(void)
{
	return m_pCamera;
}
//=============================================================================
// カメラの取得
//=============================================================================

D3DXVECTOR3  CCamera::GetCameraPos(void)
{
	return m_pos;
}

CCamera * CCamera::Create(void)
{
	CCamera *pCamera;
	pCamera = new CCamera;
	m_pCamera = pCamera;
	pCamera->InitCamera();
	return pCamera;
}
